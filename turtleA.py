import turtle

t = turtle.Turtle()

t.right(90)  # Повертаємо черепаху вправо (тобто вниз)
t.forward(100)  # Рухаємось вперед (тепер вниз на 100 пікселів)
t.right(90)  # Повертаємо черепаху вправо (тобто вниз)
t.forward(100)
t.right(90)  # Повертаємо черепаху вправо (тобто вниз)
t.forward(100)
t.left(90)
t.forward(100)
t.left(180)
t.forward(100)
t.right(90)
t.forward(100)
t.right(90)
t.forward(100)

turtle.done()